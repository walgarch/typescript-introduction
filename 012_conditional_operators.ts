let x : number = 100;

console.log(`The number set is ${x}`);

if(x == 200) {
	console.log(`Condition passed : x == 200`);
}

if(x === 100) {
	console.log(`Condition passed : x === 100`);
}

if(x != 100) {
	console.log(`Condition passed : x != 100`);
}

if(x > 100) {
	console.log(`Condition passed : x > 100`);
}

if(x >= 100) {
	console.log(`Condition passed : x >= 100`);
}

if(x < 100) {
	console.log(`Condition passed : x < 100`);
}

if(x <= 100) {
	console.log(`Condition passed : x <= 100`);
}
